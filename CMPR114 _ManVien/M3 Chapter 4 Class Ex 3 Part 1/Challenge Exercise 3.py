# This program uses the for loop to print 1-5 and
# print odd and even number to a maximum of 10.

print('I will display the numbers 1 through 5.')
for num in [1,2,3,4,5]:     # the numbers are placed in an array
    print(num)

print('\nI will display the odd numbers 1 through 9.')
for num in [1,3,5,7,9]:     # the numbers are placed in an array
    print(num)

print('\nI will display the even numbers 2 through 10.')
for num in [2,4,6,8,10]:     # the numbers are placed in an array
    print(num)
  
print()
