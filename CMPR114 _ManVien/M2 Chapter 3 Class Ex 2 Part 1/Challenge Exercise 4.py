print('Display the Roman numeral version of a number')

number = int(input('Enter a number, from 1-10: '))

if number == 1:
    print('Roman Numeral is I')
elif number == 2:
    print('Roman Numeral is II')
elif number == 3:
    print('Roman Numeral is III')
elif number == 4:
    print('Roman Numeral is IV')
elif number == 5:
    print('Roman Numeral is V')
elif number == 6:
    print('Roman Numeral is VI')
elif number == 7:
    print('Roman Numeral is VII')
elif number == 8:
    print('Roman Numeral is VIII')
elif number == 9:
    print('Roman Numeral is IX')
elif number == 10:
    print('Roman Numeral is X')
else:
    # if the number is outside the range of 1 through 10
    print('Invalid entry, try again.')
