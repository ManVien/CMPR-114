number_of_cookie = int(input('Enter the number of cookies you want to make: '))
sugar = number_of_cookie * 1.5 / 48
butter = number_of_cookie * 1 / 48
flour = number_of_cookie * 2.75 / 48
print(f'The number of cups of sugar is {sugar:.2f}')
print(f'The number of cups of butter is {butter:.2f}')
print(f'The number of cups of flour is {flour:.2f}')

